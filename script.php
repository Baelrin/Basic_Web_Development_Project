<?php
// Создание переменной и вывод на экран
$ученик = "Иванов Иван";
echo "Ученик: " . $ученик . "<br>";

// Создание массива и вывод на экран
$ученики = ["Иванов", "Петров", "Сидоров"];
echo "Ученики: " . implode(", ", $ученики) . "<br>";

// Условие if-else и вывод на экран
$погода = "солнечно";
if ($погода == "солнечно") {
    echo "Идем гулять";
} else {
    echo "Остаемся дома";
}
?>